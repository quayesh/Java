package javaPractice;

public class DoWhileLoop {

	public static void main(String[] args) {
		
		int i=1;
		
		do
		{
			System.out.println(i);
			i++;
		}while(i<=10); // condition starts here

	}
	//while vs do-while loop: while loop first check the condition then enter the block
	//Do while loop first dont ckeck the condition directly jump into the block
	//Second round do while loop verify the condition
	

}
