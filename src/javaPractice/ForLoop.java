package javaPractice;

public class ForLoop {

	public static void main(String[] args) {
		
		//Initialization, Condition, inc/dec
		//these three used inn three different statement but for loop used it one statement
		//How to print 1to10 number
		
		for(int i=1;i<=10;i++)
		{
			System.out.println(i);
		}
		//Print decending order
		for(int i=10;i>0;i--)
		{
			System.out.println(i);
		}
		//It is reduced the code
		//For loop must specify the condition(starting and ending point)
		//For exact limit we use for loop
		// No exact limit we use while loop
		

	}

}
